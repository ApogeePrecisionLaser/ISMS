package com.apogee.visitor;

public class DeviceInfoRegBean {
	
	private String active;
	private String revision_no;
	private String device_info_id;
	private String device_id;
	private String device_name;
	private String device_purpose;
	private String created_date;
	private String status;
	
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getActive() {
		return active;
	}
	public void setActive(String active) {
		this.active = active;
	}
	public String getRevision_no() {
		return revision_no;
	}
	public void setRevision_no(String revision_no) {
		this.revision_no = revision_no;
	}
	public String getDevice_info_id() {
		return device_info_id;
	}
	public void setDevice_info_id(String device_info_id) {
		this.device_info_id = device_info_id;
	}
	public String getDevice_id() {
		return device_id;
	}
	public void setDevice_id(String device_id) {
		this.device_id = device_id;
	}
	public String getDevice_name() {
		return device_name;
	}
	public void setDevice_name(String device_name) {
		this.device_name = device_name;
	}
	public String getDevice_purpose() {
		return device_purpose;
	}
	public void setDevice_purpose(String device_purpose) {
		this.device_purpose = device_purpose;
	}
	public String getCreated_date() {
		return created_date;
	}
	public void setCreated_date(String created_date) {
		this.created_date = created_date;
	}

}
